
// =========================== Lógicos ======================== //



/**
 Operadores lógicos

 && and / e  = todas as expressões precisam ser verdadeiras para retornar true
 || or / ou = Só precisa que uma seja verdadeiro para retornar true
 ! not / não = negação ele inverte o valor da expressão
 */

 const expressionAnd = true && true && true && true
 console.log(expressionAnd) // vai retornar verdadeiro

 const expressionOr = true || false || true || false
 console.log(expressionOr) // vai retornar verdadeiro

 const expressionNot = !true
 console.log(expressionNot) // vai retornar false

